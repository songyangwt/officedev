package office.jbsp.pojo;

public class JbspSummaryBean {

	private String year;
	private String name;
	private String chu;
	private String times;
	private String days;
	private String didays;
	public String getYear() {
		return year;
	}
	public void setYear(String year) {
		this.year = year;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getChu() {
		return chu;
	}
	public void setChu(String chu) {
		this.chu = chu;
	}
	public String getTimes() {
		return times;
	}
	public void setTimes(String times) {
		this.times = times;
	}
	public String getDays() {
		return days;
	}
	public void setDays(String days) {
		this.days = days;
	}
	public String getDidays() {
		return didays;
	}
	public void setDidays(String didays) {
		this.didays = didays;
	}
	
}
